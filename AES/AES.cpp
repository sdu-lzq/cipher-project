uint32_t shift_left(uint32_t num,int shift_num) {
	return num = (num << shift_num) ^ (num >> (32 - shift_num));
}

uint32_t shift_right(uint32_t num, int shift_num) {
	return num = num>>shift_num^(num<<(32-shift_num));
}



uint8_t S_replace(uint8_t in) {
	uint8_t x = in >> 4;
	uint8_t y = in << 4;
	y = y >> 4;
	int index = x * 16 + y;
	return S[index];
}

uint8_t Siv_replace(uint8_t in) {
	uint8_t x = in >> 4;
	uint8_t y = in << 4;
	y = y >> 4;
	int index = x * 16 + y;
	return SIV[index];
}

void KeyExtend() {
	uint32_t Rcon[10] = { 0x01000000,0x02000000,0x04000000,0x08000000,0x10000000,0x20000000,0x40000000,0x80000000,0x1B000000,0x36000000 };
	uint8_t *temp;
	for (int i = 4; i < 44; i++) {
		if (i % 4 == 0) {
			Key[i] = shift_left(Key[i - 1], 8);
			temp = (uint8_t*)&Key[i];
			for (int i = 0; i < 4; i++)
				temp[i] = S_replace(temp[i]);
			Key[i] = Key[i]^ Key[i - 4] ^ Rcon[i / 4 - 1];
		}
		else
			Key[i] = Key[i - 4] ^ Key[i - 1];
	}

}

void Keyinv_col() {
	uint32_t temp[44];
	for (int i = 0; i < 44; i++)
		temp[i] = Key[i];
	for (int i = 4; i < 40; i++) {
		Key[i] = Table2[S_replace(temp[i] >> 24 & 0xFF)];
		Key[i]^= shift_right(Table2[S_replace(temp[i] >> 16 & 0xFF)],8);
		Key[i] ^= shift_right(Table2[S_replace(temp[i] >> 8 & 0xFF)], 16);
		Key[i] ^= shift_right(Table2[S_replace(temp[i] >> 0 & 0xFF)], 24);
	}
}

