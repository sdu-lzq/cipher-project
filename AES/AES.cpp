uint8_t xtime(uint8_t x) {
	//00011011
	uint8_t temp = x << 1;
	return ((x >> 7) ^ 0x01) ? temp : temp ^ 27;
}

uint8_t xtime2(uint8_t x)
{
	return (xtime(x))^x;
}

uint32_t L(uint8_t x) {
	uint32_t res;
	uint32_t res1 = xtime2(S_replace(x));
	uint32_t res2 = S_replace(x) << 8;
	uint32_t res3 = S_replace(x) << 16;
	uint32_t res4 = xtime(S_replace(x)) << 24;
	res = res1 ^ res2 ^ res3 ^ res4;
	return res;
}

uint8_t Mult(uint8_t x, uint8_t y) {
	return (((y & 1) * x) ^ ((y >> 1 & 1) * xtime(x)) ^ ((y >> 2 & 1) * xtime(xtime(x))) ^ ((y >> 3 & 1) * xtime(xtime(xtime(x)))) ^ ((y >> 4 & 1) * xtime(xtime(xtime(xtime(x))))));
}

uint32_t InvL(uint8_t x) {
	uint32_t res;
	uint32_t res1 = Mult(Siv_replace(x), 0x0e)<<24;
	uint32_t res2 = Mult(Siv_replace(x),0x09) << 16;
	uint32_t res3 = Mult(Siv_replace(x),0x0d) << 8;
	uint32_t res4 = Mult(Siv_replace(x), 0x0b);
	res = res1 ^res2 ^ res3 ^ res4;
	return res;
}

void generateEnTable() {
	for (int i = 0; i <= 0xFF; i++) {
		Table[i] = L(i);
	}
}

void generateDeTable() {
	for (int i = 0; i <= 0xFF; i++) {
		Table2[i] = InvL(i);
	}
}


